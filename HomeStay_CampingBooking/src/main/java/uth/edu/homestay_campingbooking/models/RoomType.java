package uth.edu.homestay_campingbooking.models;

public enum RoomType {
    StandardRoom //Phòng tiêu chuẩn, dành cho 1-2 người, tiện nghi cơ bản
    ,DeluxeRoom //Phòng cao cấp hơn, có view đẹp, tiện ích đầy đủ.
    ,SuiteRoom // Phòng lớn, có phòng khách riêng, thường dành cho gia đình hoặc khách VIP.
    ,DormitoryRoom //Phòng tập thể, giường tầng, phù hợp nhóm bạn hoặc khách du lịch.
    ,Bungalow //Nhà nhỏ riêng biệt, gần thiên nhiên, thường gặp ở khu du lịch sinh thái.
}
